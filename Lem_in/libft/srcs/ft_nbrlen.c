int		ft_nbrlen(int n)
{
	int				i;
	unsigned int	nbr;

	nbr = (n < 0 ? -n : n);
	i = (n <= 0 ? 1 : 0);
	while (nbr > 0)
	{
		nbr = nbr / 10;
		i++;
	}
	return (i);
}
